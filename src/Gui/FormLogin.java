    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txtUsername.getText();
        String sandi = passEncrypt.getEncrypt(txtSandi.getText());
        
        String sql = "SELECT * FROM tbl_pengguna WHERE username='"+username+"' AND sandi='"+sandi+"'";
        try {
            st = con.createStatement();
            rs = st.executeQuery(sql);
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Anda login sebagai "+rs.getString("level"));
                new FormAdmin().setVisible(true);
                this.dispose();
            }
            else
                JOptionPane.showMessageDialog(null, "nama user atau sandi yang anda masukan salah !!!");
        } catch (SQLException ex) {
            Logger.getLogger(FormLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

